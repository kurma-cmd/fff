export function getAdmissionGuide(university: University): AdmissionGuide {
  const target = languageTargets[university.englishBand];
  const sat = university.country === 'США'
    ? 'Проверить: required / optional зависит от вуза'
    : satCountries.has(university.country)
      ? 'Иногда принимается — проверить программу'
      : 'Обычно не требуется';
  const [applicationWindow, checkWhen] = schedules[university.country] ?? ['Зависит от программы', 'Проверить за 6–9 месяцев до поступления'];

  return {
    ielts: `${target.ielts} (ориентир)`,
    toefl: `${target.toefl} iBT (ориентир)`,
    gpa: `${target.gpa} (конкурсный ориентир)`,
    sat,
    applicationWindow,
    checkWhen,
    note: university.country === 'Казахстан'
      ? 'Также проверь ЕНТ, внутренний экзамен или интервью.'
      : 'Официальные минимумы зависят от программы и года поступления.',
  };
}
